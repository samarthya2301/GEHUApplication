package com.samarthya.gehuapplication.ui.dashboard;

public class DashboardValuesCard {

	String dashboardField;
	String dashboardKey1;
	String dashboardKey2;
	String dashboardValue1;
	String dashboardValue2;

	public DashboardValuesCard(String dashboardField,
							   String dashboardKey1,
							   String dashboardKey2,
							   String dashboardValue1,
							   String dashboardValue2) {

		this.dashboardField = dashboardField;
		this.dashboardKey1 = dashboardKey1;
		this.dashboardKey2 = dashboardKey2;
		this.dashboardValue1 = dashboardValue1;
		this.dashboardValue2 = dashboardValue2;

	}

}
